# Operando sinistro
a = 27.5
# Operando destro
b = 2
# Somma
c = a + b
# Sottrazione
d = a - b
# Moltiplicazione
e = a * b
# Divisione
f = a / b
# Divisione intera
g = a // b
# Potenza
h = a ** b

# Stampa i valori delle espressioni
print("a + b =", c)
print("a - b =", d)
print("a * b =", e)
print("a / b =", f)
print("a // b =", g)
print("a ** b =", h)
